// Helper function to get states for a category
export const getStatesForCategory = (categoryName) => {
  const vendors = VENDORS_DATA.filter(v => v.category === categoryName);
  const states = [...new Set(vendors.map(v => v.state))];
  return states.sort();
};

// Helper function to get cities for a specific state and category
export const getCitiesForStateAndCategory = (categoryName, state) => {
  const vendors = VENDORS_DATA.filter(v => v.category === categoryName && v.state === state);
  const cities = [...new Set(vendors.map(v => v.city))];
  return cities.sort();
};

// Helper function to get vendors filtered by category, state, and city
export const getVendorsByFilters = (categoryName, state, city) => {
  return VENDORS_DATA.filter(
    v => v.category === categoryName && v.state === state && v.city === city
  );
};
